// 更新4个语言文件
languages.forEach(lang => {
  const filePath = path.join(messagesDir, `${lang}.json`);
  let messages = JSON.parse(fs.readFileSync(filePath, 'utf8'));

  if (!messages.Hr) messages.Hr = {};

  Object.keys(translations).forEach(key => {
    if (!messages.Hr[key]) {
      if (lang === 'zh-CN') messages.Hr[key] = translations[key];
      else if (lang === 'en') messages.Hr[key] = enMap[key] || translations[key];
      else if (lang === 'zh-TW') messages.Hr[key] = zhTwMap[key] || translations[key];
      else if (lang === 'vi') messages.Hr[key] = viMap[key] || translations[key];
    }
  });

  fs.writeFileSync(filePath, JSON.stringify(messages, null, 2) + '\n', 'utf8');
  console.log(`✅ Updated ${lang}.json`);
});

console.log('✅ All language files updated!');
