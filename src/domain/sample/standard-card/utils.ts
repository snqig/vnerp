export function toggleMultiValue(current: string, value: string): string {
  const arr = current ? current.split(',').filter(Boolean) : [];
  const idx = arr.indexOf(value);
  if (idx >= 0) {
    arr.splice(idx, 1);
  } else {
    arr.push(value);
  }
  return arr.join(',');
}

